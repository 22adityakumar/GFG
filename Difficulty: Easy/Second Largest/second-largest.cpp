class Solution {
  public:
    // Function returns the second
    // largest elements
    int print2largest(vector<int> &nums) {
        // Code Here
     int max = INT_MIN;
        int secondMax = INT_MIN;

        for (int num : nums) {
            if (num > max) {
                secondMax = max;
                max = num;
            } else if (num > secondMax && num != max) {
                secondMax = num;
            }
        }

        return secondMax == INT_MIN ? -1 : secondMax;
    
    }
};

